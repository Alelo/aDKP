<?php
	function class_color($str){
		switch($str){
			case DRUID:
				return "#FF7D0A";
			case WARLOCK:
				return "#9482C9";
			case HUNTER:
				return "#ABD473";
			case WARRIOR:
				return "#C79C6E";
			case MAGE:
				return "#69CCF0";
			case PALADIN:
				return "#F58CBA";
			case PRIEST:
				return "#FFFFFF";
			case SHAMAN:
				return "#2459FF";
			case ROGUE:
				return "#FFF569";
			case DEATHKNIGHT:
				return "#C41F3B";
		}
	};
?>