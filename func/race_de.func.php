<?php
	function race_de($race){
		switch($race)
		{
			case Human:
				return 'Menschen';
			case Dwarf:
				return 'Zwergen';
			case Gnome:
				return 'Gnome';
			case Draenei:
				return 'Draenei';
			case NightElf:
				return 'Nachtelf';
			case Orc:
				return 'Ork';
			case Troll:
				return 'Troll';
			case Scourge:
				return 'Untoter';
			case Tauren:
				return 'Tauren';
			case BloodElf:
				return 'Blutelf';
		}
	};
?>